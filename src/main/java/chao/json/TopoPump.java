package chao.json;

public class TopoPump{

    private Integer pumpId;

    private String topologyCode;

    private String factoryCode;

    private Integer dataPumpStatus;

    private String dataPumpCode;

    private String dataPumpName;

    private Integer number;

    public Integer getPumpId() {
        return pumpId;
    }

    public void setPumpId(Integer pumpId) {
        this.pumpId = pumpId;
    }

    public String getTopologyCode() {
        return topologyCode;
    }

    public void setTopologyCode(String topologyCode) {
        this.topologyCode = topologyCode;
    }

    public String getFactoryCode() {
        return factoryCode;
    }

    public void setFactoryCode(String factoryCode) {
        this.factoryCode = factoryCode;
    }

    public Integer getDataPumpStatus() {
        return dataPumpStatus;
    }

    public void setDataPumpStatus(Integer dataPumpStatus) {
        this.dataPumpStatus = dataPumpStatus;
    }

    public String getDataPumpCode() {
        return dataPumpCode;
    }

    public void setDataPumpCode(String dataPumpCode) {
        this.dataPumpCode = dataPumpCode;
    }

    public String getDataPumpName() {
        return dataPumpName;
    }

    public void setDataPumpName(String dataPumpName) {
        this.dataPumpName = dataPumpName;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

}
